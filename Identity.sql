CREATE TABLE PUBLIC.T_USER (
	ID VARCHAR(64) NOT NULL,
	REV INTEGER,
	FIRST_NAME VARCHAR(255),
	LAST_NAME VARCHAR(255),
	EMAIL VARCHAR(255),
	PWD VARCHAR(255),	
	CONSTRAINT CONSTRAINT_7F PRIMARY KEY (ID_)
);

CREATE UNIQUE INDEX PRIMARY_KEY_7F ON PUBLIC.T_USER (ID_);

ALTER TABLE PUBLIC.T_USER ADD ROLE_ID INTEGER NOT NULL;
ALTER TABLE PUBLIC.T_USER ADD GROUP_ID INTEGER NOT NULL;



CREATE TABLE PUBLIC.T_GROUP (
	ID VARCHAR(64) NOT NULL,
	REV INTEGER,
	NAME VARCHAR(255),
	TYPE VARCHAR(255),
	CONSTRAINT CONSTRAINT_6B PRIMARY KEY (ID)
);

CREATE UNIQUE INDEX PRIMARY_KEY_6B ON PUBLIC.T_GROUP (ID);


CREATE TABLE PUBLIC.T_ROLE(
	ID VARCHAR(64) NOT NULL,
	REV INTEGER,
	NAME VARCHAR(255),
	TYPE VARCHAR(255),
	CONSTRAINT CONSTRAINT_6B PRIMARY KEY (ID)
);

CREATE UNIQUE INDEX PRIMARY_KEY_6B ON PUBLIC.T_ROLE (ID);





CREATE TABLE PUBLIC.T_USERGROUPMAPPING (
	USER_ID  VARCHAR(64) NOT NULL,
	GROUP_ID VARCHAR(64) NOT NULL,
	CONSTRAINT CONSTRAINT_E PRIMARY KEY (USER_ID,GROUP_ID),
	CONSTRAINT ACT_FK_MEMB_GROUP FOREIGN KEY (GROUP_ID) REFERENCES PUBLIC.T_GROUP(ID) ON DELETE RESTRICT ON UPDATE RESTRICT,
	CONSTRAINT ACT_FK_MEMB_USER FOREIGN KEY (USER_ID) REFERENCES PUBLIC.T_USER(ID) ON DELETE RESTRICT ON UPDATE RESTRICT
);

CREATE INDEX ACT_FK_MEMB_GROUP_INDEX_E ON PUBLIC.T_USERGROUPMAPPING (GROUP_ID);

CREATE INDEX ACT_FK_MEMB_USER_INDEX_E ON PUBLIC.T_USERGROUPMAPPING (USER_ID);

CREATE UNIQUE INDEX PRIMARY_KEY_E ON PUBLIC.T_USERGROUPMAPPING (USER_ID,GROUP_ID);



CREATE TABLE PUBLIC.T_ROLEGROUPMAPPING (
	USER_ID  VARCHAR(64) NOT NULL,
	ROLE_ID VARCHAR(64) NOT NULL,
	CONSTRAINT CONSTRAINT_E PRIMARY KEY (USER_ID,ROLE_ID),
	CONSTRAINT ACT_FK_MEMB_GROUP FOREIGN KEY (ROLE_ID) REFERENCES PUBLIC.T_ROLE(ID) ON DELETE RESTRICT ON UPDATE RESTRICT,
	CONSTRAINT ACT_FK_MEMB_USER FOREIGN KEY (USER_ID) REFERENCES PUBLIC.T_USER(ID) ON DELETE RESTRICT ON UPDATE RESTRICT
);

CREATE INDEX ACT_FK_MEMB_GROUP_INDEX_E ON PUBLIC.T_ROLEGROUPMAPPING (ROLE_ID);

CREATE INDEX ACT_FK_MEMB_USER_INDEX_E ON PUBLIC.T_ROLEGROUPMAPPING (USER_ID);

CREATE UNIQUE INDEX PRIMARY_KEY_E ON PUBLIC.T_ROLEGROUPMAPPING (USER_ID,ROLE_ID);









